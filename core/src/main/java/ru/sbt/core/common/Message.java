package ru.sbt.core.common;


import ru.sbt.core.Domain;

public class Message implements Domain{
    private String from;
    private String to;

    public Message(String from, String to) {
        this.from = from;
        this.to = to;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }
}
